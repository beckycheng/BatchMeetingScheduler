    {
        $user = auth()->user();
        $meetings = Meeting::with('participants')
            ->whereHas('participants', fn($query) => $query->where('username', $user->name))
            ->orWhere('moderator', $user->id)
            ->get();
        return view('meeting.index', compact('meetings'));
    }

